// ═════════════════════════════════════════════════════════════
// Theme Data Class
// ═════════════════════════════════════════════════════════════

data class AppTheme(
    val id: String,
    val name: String,
    val colorScheme: ColorScheme,
    val isDark: Boolean = true,
)

// All available themes
val AVAILABLE_THEMES = listOf(
    AppTheme("default_dark", "Dark (Default)", DarkThemeColors),
    AppTheme("purple_night", "Purple Night", PurpleThemeColors),
    AppTheme("ocean", "Ocean Green", OceanThemeColors),
    AppTheme("sunset", "Sunset Orange", SunsetThemeColors),
    AppTheme("mint", "Mint Fresh", MintThemeColors),
    AppTheme("pink", "Pink Neon", PinkThemeColors),
    AppTheme("gold", "Gold Luxe", GoldThemeColors),
    AppTheme("cyberpunk", "Cyberpunk", CyberpunkThemeColors),
)

fun getThemeById(id: String): AppTheme {
    return AVAILABLE_THEMES.find { it.id == id } ?: AVAILABLE_THEMES[0]
}
